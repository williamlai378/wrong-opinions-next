export const yearOptions = () => {
    let arr = [];
    let currYear = new Date().getFullYear();
    let diff = currYear - 1940;
    for(let i = 0; i <= diff; i++) {
        let yearObj = {value: currYear - i, label: (currYear - i)};
        arr.push(yearObj)
    }

    return arr;
}

